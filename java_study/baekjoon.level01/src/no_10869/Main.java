package no_10869;

import java.util.Scanner;

public class Main {
	public static void main(String[] args) {
//		두 자연수 A와 B가 주어진다. 이때, A+B, A-B, A*B, A/B(몫), A%B(나머지)를 출력하는 프로그램을 작성하시오. 
		Scanner sc = new Scanner(System.in);
		int a, b;
		a = sc.nextInt();
		b = sc.nextInt();
		if (1 <= a && a <= 10000) {
			if (1 <= b && b <= 10000) {
				System.out.println(a + b);
				System.out.println(a - b);
				System.out.println(a * b);
				System.out.println(a / b);
				System.out.println(a % b);
			}
		}

	}

}
